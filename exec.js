function getGroupCounts() {
  const counts = {};
  DEPARTMENTS.forEach((d) => {
    counts[d.group] = (counts[d.group] || 0) + 1;
  });
  return counts;
}

function getPriorityCounts(groupFilter) {
  const counts = {};
  DEPARTMENTS
    .filter((d) => groupFilter === 'all' || d.group === groupFilter)
    .forEach((d) => { counts[d.priority] = (counts[d.priority] || 0) + 1; });
  return counts;
}

function buildFilterBtns(containerId, items, activeKey, onClick) {
  const bar = document.getElementById(containerId);
  if (!bar) return;
  bar.innerHTML = '';
  items.forEach(([key, label, count]) => {
    const btn = document.createElement('button');
    btn.className = 'filter-btn' + (key === activeKey ? ' active' : '');
    btn.innerHTML = label + (count != null ? `<span class="count">${count}</span>` : '');
    btn.addEventListener('click', () => onClick(key));
    bar.appendChild(btn);
  });
}

function renderGroupFilters() {
  const counts = getGroupCounts();
  const items = [['all', 'Все', DEPARTMENTS.length]];
  Object.entries(GROUP_META).forEach(([key, meta]) => {
    if (key === 'all') return;
    if (counts[key]) items.push([key, meta.label, counts[key]]);
  });
  buildFilterBtns('groupFilterBar', items, activeGroup, (key) => {
    activeGroup = key;
    activePriority = 'all';
    renderFilters();
    renderDeptCards();
  });
}

function renderPriorityFilters() {
  const counts = getPriorityCounts(activeGroup);
  const total = Object.values(counts).reduce((s, n) => s + n, 0);
  const items = [
    ['all', 'Все', total],
    ['high', 'HIGH', counts.high || 0],
    ['medium', 'MEDIUM', counts.medium || 0],
    ['low', 'LOW', counts.low || 0],
  ].filter(([k, , c]) => k === 'all' || c > 0);
  buildFilterBtns('priorityFilterBar', items, activePriority, (key) => {
    activePriority = key;
    renderFilters();
    renderDeptCards();
  });
}

function renderFilters() {
  renderGroupFilters();
  renderPriorityFilters();
}

