function updateDeptStats() {
  const counts = { high: 0, medium: 0, low: 0 };
  DEPARTMENTS.forEach((d) => counts[d.priority]++);

  const el = (id) => document.getElementById(id);
  if (el('statHigh'))   el('statHigh').textContent   = counts.high;
  if (el('statMedium')) el('statMedium').textContent = counts.medium;
  if (el('statLow'))    el('statLow').textContent    = counts.low;
  if (el('statTotal'))  el('statTotal').textContent  = DEPARTMENTS.length;
}

