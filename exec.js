function getFilteredDepts() {
  return DEPARTMENTS.filter((d) => {
    const groupOk    = activeGroup === 'all'    || d.group === activeGroup;
    const priorityOk = activePriority === 'all' || d.priority === activePriority;
    return groupOk && priorityOk;
  });
}

function priorityBadge(priority) {
  const map = {
    high:   ['HIGH',   'priority-high'],
    medium: ['MEDIUM', 'priority-medium'],
    low:    ['LOW',    'priority-low'],
  };
  const [text, cls] = map[priority] || ['', ''];
  return `<span class="priority-badge ${cls}">${text}</span>`;
}

function groupTag(dept) {
  const cls = GROUP_META[dept.group]?.cls || 'group-other';
  return `<span class="dept-group-tag ${cls}">${dept.group_label}</span>`;
}

function taskBadge(task) {
  const parts = task.split('+');
  return parts.map((t) => {
    const key = t.trim();
    return `<span class="dept-task-badge task-${key}">${key}</span>`;
  }).join('');
}

