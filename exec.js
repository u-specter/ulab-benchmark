function renderDeptCards() {
  const grid = document.getElementById('deptGrid');
  if (!grid) return;
  grid.innerHTML = '';

  const filtered = getFilteredDepts();

  const count = document.getElementById('resultsCount');
  if (count) {
    count.textContent = `Показано: ${filtered.length} из ${DEPARTMENTS.length} департаментов`;
  }

  if (filtered.length === 0) {
    grid.innerHTML = `
      <div class="dept-empty">
        <div class="dept-empty-icon">🔍</div>
        <div class="dept-empty-text">Нет департаментов по выбранным фильтрам</div>
      </div>
    `;
    return;
  }

  // Sort: high → medium → low
  const priorityOrder = { high: 0, medium: 1, low: 2 };
  const sorted = [...filtered].sort(
    (a, b) => priorityOrder[a.priority] - priorityOrder[b.priority]
  );

  sorted.forEach((dept) => grid.appendChild(createDeptCard(dept)));
}

